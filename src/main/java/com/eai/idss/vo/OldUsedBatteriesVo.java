package com.eai.idss.vo;

public class OldUsedBatteriesVo {

	private double twoWheelerOld;
	private double fourWheelerOld;
	private double upsOld;
	private double motivePowerOld;
	private double standByOld;
	private double otherInvertersOld;
	public double getTwoWheelerOld() {
		return twoWheelerOld;
	}
	public void setTwoWheelerOld(double twoWheelerOld) {
		this.twoWheelerOld = twoWheelerOld;
	}
	public double getFourWheelerOld() {
		return fourWheelerOld;
	}
	public void setFourWheelerOld(double fourWheelerOld) {
		this.fourWheelerOld = fourWheelerOld;
	}
	public double getUpsOld() {
		return upsOld;
	}
	public void setUpsOld(double upsOld) {
		this.upsOld = upsOld;
	}
	public double getMotivePowerOld() {
		return motivePowerOld;
	}
	public void setMotivePowerOld(double motivePowerOld) {
		this.motivePowerOld = motivePowerOld;
	}
	public double getStandByOld() {
		return standByOld;
	}
	public void setStandByOld(double standByOld) {
		this.standByOld = standByOld;
	}
	public double getOtherInvertersOld() {
		return otherInvertersOld;
	}
	public void setOtherInvertersOld(double otherInvertersOld) {
		this.otherInvertersOld = otherInvertersOld;
	}

}
